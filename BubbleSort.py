print('Bubble Sort')
a = [54, 26, 93, 17, 77, 31, 15, 44, 55, 20]
print(a)


def bubble_sort(list_a):
    list_length = len(list_a)
    for i in range(0, list_length - 1):
        for i in range(0, list_length - 1):
            if list_a[i] > list_a[i + 1]:
                list_a[i], list_a[i + 1] = list_a[i + 1], list_a[i]
    return list_a


print(bubble_sort(a))
